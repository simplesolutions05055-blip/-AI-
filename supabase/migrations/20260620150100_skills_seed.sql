-- ============================================================================
-- Seed the Skills Registry from the client's unified spec
-- (10 skills + 10 agents + 10 rules). Each gets an initial active version (v1).
-- Idempotent: skips a key that already exists, so re-running won't duplicate.
-- ============================================================================

create or replace function public.seed_skill(
  p_key text, p_name text, p_desc text, p_category text, p_order int, p_content text
) returns void language plpgsql as $$
begin
  if exists (select 1 from public.skills where key = p_key) then
    return;
  end if;
  insert into public.skills (key, display_name, description, category, order_index)
  values (p_key, p_name, p_desc, p_category, p_order);
  insert into public.skill_versions (skill_key, version_number, content, is_active, note)
  values (p_key, 1, p_content, true, 'גרסה ראשונית מהמסמך המאוחד');
end;
$$;

